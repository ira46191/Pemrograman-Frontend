/**
 * mengecek status dewasa
 */

const age = 16;
if (age > 21) {
  console.log("Dewasa");
} else {
  console.log("Belum Dewasa");
}

/**
 * ternary operator digunakan untuk membuat if else lebih singkat
 */
age > 21 ? console.log("Dewasa") : console.log("Belum Dewasa");

